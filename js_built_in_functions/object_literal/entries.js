/*

Object.entries() method returns the keys and values of the object as an Array

*/


const myObj3 = {
    name: 'Rocky',
    age: 25,
    favoriteLanguage: 'JavaScript',
    sport: 'Soccer'
}

console.log(Object.entries(myObj3))

/*

[
    ['name', 'Rocky'],
    ['age', 25],
    ['favoriteLanguage', 'JavaScript'],
    ['sport', 'Soccer']
]

*/